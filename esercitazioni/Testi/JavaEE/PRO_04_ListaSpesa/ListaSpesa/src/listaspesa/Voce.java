package listaspesa;

public class Voce {

	private String messaggio;

	public Voce(String messaggio) {
		this.messaggio = messaggio;
	}

	/**
	 * @return the messaggio
	 */
	public String getMessaggio() {
		return messaggio;
	}

	/**
	 * @param messaggio the messaggio to set
	 */
	public void setMessaggio(String messaggio) {
		this.messaggio = messaggio;
	}
	
	
	
}
