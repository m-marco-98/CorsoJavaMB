public class Char05
{
   public static void main(String[] args)
   {
      char x;
      char y;
 
      x = 'a';
      y = (char) (x - 32);
      System.out.println("x = '" + x + "',  y = '" + y + "'");

      x = 'b';
      y = (char) (x - 32);
      System.out.println("x = '" + x + "',  y = '" + y + "'");

      x = 'c';
      y = (char) (x - 32);
      System.out.println("x = '" + x + "',  y = '" + y + "'");
   }
}

