public class Char01B
{
   public static void main(String[] args)
   {
      char a;
 
      a = 65;                  // Char code for the character 'A'
      System.out.println(a);

      int  i;

      i = 'A' + 1;             // 'A' is just an integer !!!
      System.out.println(i);   // Prints 66 !!!
   }
}

